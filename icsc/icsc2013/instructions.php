STEP 3: Please send us the PDF generated by IEEE PDF eXpress Plus and source file of the paper at EDAS.<br>
For Signal Processing Papers email to : vijay.khare@jiit.ac.in
<br>For Communication Papers email to : vivek.dwivedi@jiit.ac.in
<br>For VLSI Technology and Embedded Systems Papers email to : manish.kumar@jiit.ac.in
