<div class="okrdx-acc-container">
<p><span class="exc" style="text-align: justify;">
STEP 1: Download the Camera Ready Format for ICSC 2013.<br>
<a href="IEEE_format_ICSC13.doc">Click here : (WORD Template (letter))</a>
